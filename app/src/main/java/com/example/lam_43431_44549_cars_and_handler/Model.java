package com.example.lam_43431_44549_cars_and_handler;

public class Model {
    private String name;
    private int image;

    public Model(String name, int image){
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public int getImage() {
        return image;
    }
}
