package com.example.lam_43431_44549_cars_and_models;

public class Car {

    String name;
    int image;

    public Car(String name, int image){
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public int getImage() {
        return image;
    }
}
